    void PreencherDados() {
        modelo.setNumRows(0);
        Convidado c = new Convidado();
        for (Convidado el : c.getConvidados(txtBusca.getText())) {            
            modelo.addRow(new Object[]{
                el.getId(),
                el.getNome(),
                el.getCpf(),
                el.getIdade(),
                el.getSituacao()
            });
        }
    }

