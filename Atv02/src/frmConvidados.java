    private void btnDetalhesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetalhesActionPerformed
        int linha = tabela_convidados.getSelectedRow();
        Convidado c = new Convidado();
        c = c.getConvidado(Integer.parseInt(tabela_convidados.getValueAt(linha, 0).toString()));        
            
        JOptionPane.showMessageDialog(null, "Nome: " + c.getNome()
                + "\nCPF: " + c.getCpf()
                + "\nIdade: " + c.getIdade()
                + "\nSituação: " + c.getSituacao());
    }//GEN-LAST:event_btnDetalhesActionPerformed

