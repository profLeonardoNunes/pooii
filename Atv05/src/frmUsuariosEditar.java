    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed

        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo [Nome] está vazio!");
        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo [Email] está vazio!");
        } else if (txtLogin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo [Login] está vazio!");
        } else if (txtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo [Senha] está vazio!");
        } else if (txtConfirmaSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo [Confirma Senha] está vazio!");
        } else {
            if (!txtSenha.getText().equals(txtConfirmaSenha.getText())) {
                JOptionPane.showMessageDialog(rootPane, "A senha e a confirmação estão diferentes.");
            } else {
                usuario.setNome(txtNome.getText());
                usuario.setEmail(txtEmail.getText());
                usuario.setLogin(txtLogin.getText());
                usuario.setSenha(txtSenha.getText());
                usuario.AtualizarUsuario();
                JOptionPane.showMessageDialog(rootPane, "Usuário atualizado com sucesso.");
                this.dispose();
            }
        }

    }//GEN-LAST:event_btnGravarActionPerformed

