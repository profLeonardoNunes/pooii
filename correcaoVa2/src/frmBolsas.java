    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tabelaBolsas.getSelectedRow();
        Aluno a = new Aluno();
        a.setId(Integer.parseInt(tabelaBolsas.getValueAt(linha, 0).toString()));
        a.setNome(tabelaBolsas.getValueAt(linha, 1).toString());
        a.setMedia(Double.parseDouble(tabelaBolsas.getValueAt(linha, 2).toString()));
        a.setRenda(Double.parseDouble(tabelaBolsas.getValueAt(linha, 3).toString()));
        a.setFrequencia(Double.parseDouble(tabelaBolsas.getValueAt(linha, 4).toString()));

        frmAlunoEditar frm = new frmAlunoEditar(a);
        frm.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int linha = tabelaBolsas.getSelectedRow();
        Aluno a = new Aluno();
        a.setId(Integer.parseInt(tabelaBolsas.getValueAt(linha, 0).toString()));
        a.setNome(tabelaBolsas.getValueAt(linha, 1).toString());
        int op = JOptionPane.showConfirmDialog(rootPane, "Você realmente deseja "
                + "apagar o aluno " + a.getNome() + "?");
        if(op == 0){
            a.Deletar(a.getId());
            JOptionPane.showMessageDialog(rootPane, "Aluno deletado com sucesso!");
        }
        PreencherTabela();
        

    }//GEN-LAST:event_btnDeletarActionPerformed

