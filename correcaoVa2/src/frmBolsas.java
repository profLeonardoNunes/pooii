    void ModelarTabela() {
        modelo.addColumn("Id");
        modelo.addColumn("Aluno");
        modelo.addColumn("Média das notas");
        modelo.addColumn("Renda familiar");
        modelo.addColumn("Frequencia");
        modelo.addColumn("Bolsa concedida?");
        tabelaBolsas.setModel(modelo);

    }

    void PreencherTabela() {
        modelo.setNumRows(0);
        Aluno aluno = new Aluno();
        for (Aluno a : aluno.Consultar(txtBuscar.getText())) {
            String bolsa = "NÃO";
            if (a.getMedia() >= 70 && a.getRenda() <= 3577.2 && a.getFrequencia() >= 80) {
                bolsa = "SIM";
            }
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getMedia(),
                a.getRenda(),
                a.getFrequencia(),
                bolsa
            });
        }
    }

