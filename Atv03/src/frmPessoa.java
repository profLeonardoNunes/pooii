    void PreencherTabela() {

        /*
        Rendimentos anuais at√© 21.453,24 isento
de R$ 21.453,24 a R$ 32.151,48 7,00%
de R$ 32.151,49 a R$ 42.869,16 15,00%
de 42.869,17 a R$ 53.565,72 22,5%
Acima de 53.565,72 27,5%
         */
        for (Pessoa p : new Pessoa().getPessoas("")) {
            Double imposto = 0.0;
            String cpfcpnpj = "";
            if (p.getCpf() != null) {
                cpfcpnpj = p.getCpf();
            } else {
                cpfcpnpj = p.getCnpj();
            }

            if (p.getTppessoa().equals("j")) {
                imposto = p.getRendaAnual() * 0.1;
            } else {
                if (p.getRendaAnual() <= 21453.24) {
                    imposto = 0.0;
                } else if (p.getRendaAnual() > 21453.24 && p.getRendaAnual() <= 32151.48) {
                    imposto = p.getRendaAnual() * 0.07;
                } else if (p.getRendaAnual() > 32151.48 && p.getRendaAnual() <= 42869.16) {
                    imposto = p.getRendaAnual() * 0.15;
                } else if (p.getRendaAnual() > 42869.16 && p.getRendaAnual() <= 53565.72) {
                    imposto = p.getRendaAnual() * 0.225;
                } else {
                    imposto = p.getRendaAnual() * 27.5;
                }
            }
            modelo.addRow(new Object[]{
                p.getNome(),
                cpfcpnpj,
                p.getRendaAnual(),
                imposto
            });
        }

    }

