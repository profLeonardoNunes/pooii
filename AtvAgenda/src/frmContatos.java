    void PreencherTabela(){
        modelo.setNumRows(0);
        Contato contato = new Contato();
        for(Contato c : contato.Consultar(txtBusca.getText())){
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getTel(),
                c.getCel(),
                c.getEndereco()
            });
            
        }
    }

