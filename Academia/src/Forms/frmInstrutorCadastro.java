    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        InstrutorController ic = new InstrutorController();
        Instrutor i = new Instrutor();
        i.setNome(txtNome.getText());
        i.setCpf(txtCpf.getText());
        i.setRg(txtRg.getText());
        i.setCtps(txtCtps.getText());
        i.setCargaHoraria(Double.parseDouble(txtCargaHoraria.getText()));
        i.setLogin(txtLogin.getText());
        i.setSenha(txtSenha.getText());
        ic.Cadastrar(i);
        JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso");
        
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

